def area(a,h):
    '''передаются два аргумента, выводится произведение, поделенное на два
    При входных: 4 и 2
    Output: 3'''
    return (a*h) / 2

def perimeter(a, b, c):
    '''принимаются три аргумента, выводится их сумма
    При входных: 2, 4, 3
    Output: 9'''
    return a+b+c